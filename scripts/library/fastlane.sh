fastlane_version()
{
  local FASTLANE_VERSION=$(fastlane --version)
  FASTLANE_VERSION="${FASTLANE_VERSION#fastlane}"
  FASTLANE_VERSION=$(trim $FASTLANE_VERSION)
  echo $FASTLANE_VERSION
}

fastlane_new_line()
{
  local OUTPUT=$FASTLANE_FASTFILE

  if [[ ! -z $1 ]]; then
    OUTPUT=$1
  fi

  echo '' >> $OUTPUT
}

fastlane_env_variable()
{
  local OUTPUT=$FASTLANE_FASTFILE

  if [[ ! -z $3 ]]; then
    OUTPUT=$3
  fi

  echo '    ENV["'$1'"] = "'$2'"' >> $OUTPUT
}
